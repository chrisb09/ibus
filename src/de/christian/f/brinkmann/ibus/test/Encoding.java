	static int[] getEncoding(int encodingLevel) {
		// 16l: 4bit: 6x rep
		// 256l: 8bit: 3x rep
		// 65536: 16-bit: 9bit CRC8
		if (encodingLevel == 1) {
			int[] array = new int[16];
			for (int i = 0; i < 16; i++) {
				int a = 0;
				for (int j = 0; j < 6; j++) {
					a += (i << j * 4);
				}
				array[i] = a;
			}
			return array;
		} else if (encodingLevel == 2) {
			int[] array = new int[256];
			for (int i = 0; i < 256; i++) {
				int a = 0;
				for (int j = 0; j < 3; j++) {
					a += (i << j * 8);
				}
				array[i] = a;
			}
			return array;
		}
		return null;
	}

